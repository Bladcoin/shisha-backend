exports.findAll = (req, res) => {
  const wallet = "0";
  var condition = wallet ? { wallet: { [Op.like]: `%${wallet}%` } } : null;
  User.findAll({ where: condition })
    .then(data => {
      res.send(data);
    })
    .catch(err => {
      res.status(500).send({
        message:
          err.message || "Some error occurred while retrieving tutorials."
      });
    });
};

